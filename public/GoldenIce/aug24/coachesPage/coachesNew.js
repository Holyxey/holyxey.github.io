let maxPersonCount = 8
const showAll = (button) => {
    switch (button.getAttribute('data-status')) {
        case 'hidden':
            button.innerHTML = 'Скрыть'
            button.setAttribute('data-status', 'visible')
            document.querySelectorAll('[data-spoiler="true"]').forEach((el, ind) => {
                el.style.display = 'flex'
                if (ind === 1) {
                    el.scrollIntoView({behavior: "smooth"})
                }
            })
            break
        case 'visible':
            button.setAttribute('data-status', 'hidden')
            button.innerHTML = 'Показать всех'
            document.querySelectorAll('[data-spoiler="true"]').forEach((el, ind) => {
                el.style.display = 'none'
            })
    }
}
const openCloseCoachInfo = () => {
    if (document.getElementById('coachInfo')) {
        document.getElementById('coachInfo').remove()
        document.getElementById('closeCoachInfo').remove()
    }
}
const coachInfo = (coachEl) => {
    const name = coachEl.getAttribute('data-person')
    const photo = coaches[name].photo
    const post = coaches[name].post
    const arenas = coaches[name].arenas
    const description = coaches[name].description
    const phone = coaches[name].contacts[0]
    const email = coaches[name].contacts[1]

    document.body.insertAdjacentHTML('afterbegin', `<section id="coachInfo"></section>`)
    document.getElementById('coachInfo').insertAdjacentHTML('beforeend', `<article></article>`);
    document.body.insertAdjacentHTML('afterbegin', `<svg onclick="openCloseCoachInfo()" id="closeCoachInfo" width="40" height="40" viewBox="0 0 24 24" fill="none" xmlns="http://www.w3.org/2000/svg"><g stroke-width="0"/><g stroke-linecap="round" stroke-linejoin="round"/><path fill-rule="evenodd" clip-rule="evenodd" d="M1.25 7.25a6 6 0 0 1 6-6h9.5a6 6 0 0 1 6 6v9.5a6 6 0 0 1-6 6h-9.5a6 6 0 0 1-6-6zm5.933-.067a.625.625 0 0 1 .884 0L12 11.117l3.934-3.934a.625.625 0 1 1 .883.884L12.883 12l3.934 3.934a.625.625 0 1 1-.883.883L12 12.883l-3.934 3.934a.625.625 0 1 1-.883-.883L11.117 12 7.183 8.067a.625.625 0 0 1 0-.884" fill="#232323"/></svg>`)

    const section = document.getElementById('coachInfo')
    const inTo = document.querySelector('#coachInfo > article');
    inTo.insertAdjacentHTML('beforeend', `<img src="${photo}">`)
    inTo.insertAdjacentHTML('beforeend', `<h5>${name}</h5>`)
    post ? inTo.insertAdjacentHTML('beforeend', `<h4>${post}</h4>`) : ''
    arenas[0].length > 2
        ? inTo.insertAdjacentHTML('beforeend', `<div id="coachArenas"><h6>Преподает на аренах: </h6><p>${arenas}</p></div>`)
        : ''
    inTo.insertAdjacentHTML('beforeend', `<p>${description}</p>`)
    inTo.insertAdjacentHTML('beforeend',
        `<div id="coachButtons">
        <a href="#popup:freeFirstLesson">Записаться</a>
        ${phone ? `<a href="tel:${phone}">Позвонить</a>` : ''}
        ${email ? `<a href="mailto:${email}">Написать письмо</a>` : ''}
        </div>`
    )
}
const addPerson = (person, position) => {
    if (!document.querySelector(`[data-position="${position}"]`)) {
        coachesSection.insertAdjacentHTML('afterbegin', `<h2 class="addSplash">${position}</h2><section data-person-counter="-1" data-position="${position}"></section>`)
    }
    const pasteTo = document.querySelector(`[data-position="${position}"]`)

    let isNumber = pasteTo.getAttribute('data-person-counter')
    isNumber++
    pasteTo.setAttribute('data-person-counter', isNumber++)

    if (isNumber > maxPersonCount && !pasteTo.querySelector('.showAppButton')) {
        pasteTo.insertAdjacentHTML('beforeend', `<div data-status="hidden" onclick="showAll(this)" class="showAppButton">Посмотреть всех</div>`)
    }

    pasteTo.insertAdjacentHTML('beforeend', `
        <article ${isNumber > maxPersonCount ? ' style="display:none" data-spoiler="true" ' : ''} onclick="coachInfo(this)" data-person="${person.name}">
            <img src="${person.photo}" alt="">
            <div class="personInfo">
                <h4>${person.name}</h4>
            </div>
        </article>
    `)

}
