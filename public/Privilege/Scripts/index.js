const mathRandom = (max) => {
    return Math.floor(Math.random() * max)
}
const imageAspect = (img) => {
    if (img.clientWidth < img.clientHeight) {
        // img.style.maxWidth = img.parentElement.clientWidth + 'px'
        img.style.minWidth = img.parentElement.clientWidth + 'px'
        img.style.top = '50%'
        img.style.left = '50%'
        img.style.translate = '-50% -50%'
    } else {
        // img.style.maxHeight = img.parentElement.clientHeight + 'px'
        img.style.minHeight = img.parentElement.clientHeight + 'px'
        img.style.top = '50%'
        img.style.left = '50%'
        img.style.translate = '-50% -50%'
    }
}

const servicesSliderSetup = () => {
    const servicesNode = document.getElementById('services')
    const image = servicesNode.querySelector('.sliderImage')
    // const sliderInfoBlock = servicesNode.querySelector('.sliderInfoBlock')
    const sliderButtons = servicesNode.querySelector('.sliderButtons')
    const firstService = lists.services[0]

    sliderButtons.insertAdjacentHTML('beforebegin',
        `<h5 class="sliderHeader" data-service-name="${firstService.title}">${firstService.title}</h5>`)
    firstService.textBlocks.forEach((block, index) => {
        sliderButtons.insertAdjacentHTML('beforebegin',
            `<p class="sliderTextElement" data-service-name="${firstService.title}">${block}</p>`)
    })
    if (firstService.link) {
        sliderButtons.insertAdjacentHTML('beforebegin',
            `<a href="${firstService.link}" target="_blank" class="button buttonFirst" data-service-name="${firstService.title}">Узнать подробнее</a>`)
    }
    image.style.backgroundImage = `url(${firstService.image})`
    image.style.transform = 'rotate(0) translate(0, 0)'
    servicesNode.setAttribute('data-service-counter', '0')
    servicesNode.setAttribute('data-list', `${servicesNode.id}`)
}
const changeService = (counter, list, direction) => {
    const servicesNode = document.getElementById(list)
    const image = servicesNode.querySelector('.sliderImage')
    const sliderInfoBlock = servicesNode.querySelector('.sliderInfoBlock')
    const sliderButtons = servicesNode.querySelector('.sliderButtons')
    const activeService = lists[list][counter]
    const activeElements = servicesNode.querySelectorAll('[data-service-name]')

    activeElements.forEach((element, index) => {
        const hh = element.getBoundingClientRect().height
        element.style.height = `${hh}px`
        setTimeout(() => {
            element.style.transition = 'all .5s ease-in-out'
            element.style.padding = '0 !important'
            element.style.margin = '0 !important'
            element.style.translate = `0 0`
            element.style.height = '0px'
            element.style.opacity = '0.1'
        },10)
        setTimeout(() => {
            element.remove()
        }, 300)
    })

    image.style.animation = `changeImage${direction} 1s ease-in-out reverse`

    setTimeout(() => {
        sliderButtons.insertAdjacentHTML('beforebegin',
            `<h5 class="sliderHeader" data-service-name="${activeService.title}">${activeService.title}</h5>`)
        activeService.textBlocks.forEach((block, index) => {
            sliderButtons.insertAdjacentHTML('beforebegin',
                `<p class="sliderTextElement" data-service-name="${activeService.title}">${block}</p>`)
        })
        if (activeService.link) {
            sliderButtons.insertAdjacentHTML('beforebegin',
                `<a href="${activeService.link}" target="_blank" class="button buttonFirst" data-service-name="${activeService.title}">Узнать подробнее</a>`)
        }
        image.style.backgroundImage = `url(${activeService.image})`
        image.style.animation = `changeImage${direction} 1s ease-in-out`
    },200)
    setTimeout(() => {
        image.style.animation = ''
    }, 900)
}

