const leftArrowClick = (arrow, listObject) => {
    const servicesNode = document.getElementById(listObject)
    const counter = Number(servicesNode.getAttribute('data-service-counter'))
    const list = servicesNode.getAttribute('data-list')

    document.querySelector(`#${listObject}`).scrollIntoView()

    setTimeout(() => {
        if (!lists[list][counter - 1]) {
            servicesNode.setAttribute('data-service-counter', `${lists[list].length - 1}`)
            changeService(lists[list].length - 1, listObject, 'Left')
        } else {
            servicesNode.setAttribute('data-service-counter', (counter - 1).toString())
            changeService(counter - 1, listObject, 'Left')
        }
    }, 80)
}
